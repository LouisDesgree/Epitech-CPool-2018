int add(int n1, int n2)
{
    int result = n1 + n2;
    return (result);
}

int sub(int n1, int n2)
{
    int result = n1 - n2;
    return (result);
}

int mul(int n1, int n2)
{
    int result = n1 * n2;
    return (result);
}

int div(int n1, int n2)
{
    int result = n1 / n2;
    return (result);
}

int mod(int n1, int n2)
{
    int result = n1 % n2;
    return (result);
}